/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();

  while (1)
  {
      if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET) {
          button_state = 1;
      } else {
          button_state = 0;
      }

      if (button_state == 1) {
          mode = (mode + 1) % 3;
          HAL_Delay(100);
      }

      int delay_ms = 500;
      if (mode == 1) {
          delay_ms = 250;
      } else if (mode == 2) {
          delay_ms = 100;
      }

      if (clockwise) {
          for(int i = 12; i <= 15; i++) {
              HAL_GPIO_TogglePin(GPIOD, (1 << i));
              HAL_Delay(delay_ms);
              HAL_GPIO_TogglePin(GPIOD, (1 << i));
          }
      } else {
          for(int i = 15; i >= 12; i--) {
              HAL_GPIO_TogglePin(GPIOD, (1 << i));
              HAL_Delay(delay_ms);
              HAL_GPIO_TogglePin(GPIOD, (1 << i));
          }
      }
  }
}

